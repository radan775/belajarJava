public class TipeDataBoolean {
    public static void main(String[] args) {
        // tipe data boolean memiliki 2 nilai yaitu true dan false
        // jika kita tidak memberi deklarasi nilai pada sebuah boolean maka secara default nilainya adalah false

        boolean benar = true;
        boolean salah = false;

        System.out.println(benar);
        System.out.println(salah);

    }
}
